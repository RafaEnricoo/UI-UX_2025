void main() {
  double celcius = 30;

  // Konversi ke Reamur
  double reamur = (4 / 5) * celcius;

  // Konversi ke Fahrenheit
  double fahrenheit = (9 / 5) * celcius + 32;

  print("---------------------------------------------------------");
  print("                     KONVERSI SUHU                       ");
  print("---------------------------------------------------------");
  print("Suhu dalam Celcius     : $celcius °C");
  print("Suhu dalam Reamur      : $reamur °R");
  print("Suhu dalam Fahrenheit  : $fahrenheit °F");
}
